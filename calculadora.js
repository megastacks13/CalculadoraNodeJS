
"use strict";

module.exports = {
    add: function(a, b){
        return a + b;
    },
    subtract: function(a, b){
        return a - b;
    },
    divide: function(a, b){
        return a / b;
    },
    multiply: function(a, b){
        return a * b;
    },
    root: function(a){
        return Math.sqrt(a);
    },
    square: function(a){
        return a*a;
    }
};